# Task 2: Sum of Integers from 1 to 50 Using a Loop

# Step 1: Initialize sum variable
total_sum = 0

# Step 2: Use a for loop to add numbers from 1 to 50
for number in range(1, 51):
    total_sum += number

# Step 3: Display the final sum
print("The sum of numbers from 1 to 50 is:", total_sum)
